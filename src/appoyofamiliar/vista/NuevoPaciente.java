    private boolean buscarUsuario(){
        String id = nombreBox.getText();
        boolean retorno = false;
        for (int i = 0; i < usuarios.size(); i++){
            Usuario marcado = usuarios.get(i);
            if (id.toLowerCase().equals(marcado.getIdentificador().toLowerCase())){
                retorno = true;
            }
        }
        return retorno;
    }
